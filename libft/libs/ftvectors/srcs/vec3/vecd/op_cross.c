#include "ftvectors/vec3/t_vec3_t.h"

/*
**	E0 = {1, 0, 0}
**	E1 = {0, 1, 0}
**	E2 = {0, 0, 1}
**
**	V0 = {x0, y0, z0}
**	V1 = {x1, y1, z1}
**
**	V2 = (V0 X V1)
**	   = cross(V0, V1)
**		 | E0 E1 E2 |
**	   = | x0 y0 z0 |
**		 | x1 y1 z1 |
**	   = (y0 * z1 - y1 * z0) * E0
**		-(x0 * z1 - x1 * z0) * E1
**		+(x0 * y1 - x1 * y0) * E2
**	   = {
**			y0 * z1 - y1 * z0,
**			x1 * z0 - x0 * z1,
**			x0 * y1 - x1 * y0 }
**	   = {
**			v1.y * v2.z - v2.y * v1.z,
**			v2.x * v1.z - v1.x * v2.z,
**			v1.x * v2.y - v2.x * v1.y }
*/

t_vec3	v3_cross(t_vec3 *v1, t_vec3 *v2)
{
	t_vec3	v;

	v.x = v1->y * v2->z - v1->z * v2->y;
	v.y = v1->z * v2->x - v1->x * v2->z;
	v.z = v1->x * v2->y - v1->y * v2->x;
	return (v);
}
