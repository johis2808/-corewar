#include "ftvectors/vec4/t_vec4_t.h"

t_dbl__	v4i_dot(t_vec4i *v1, t_vec4i *v2)
{
	return (v1->x * v2->x + v1->y * v2->y + v1->z * v2->z + v1->w * v2->w);
}
