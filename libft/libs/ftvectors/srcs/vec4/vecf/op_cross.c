#include "ftvectors/vec4/t_vec4_t.h"

t_vec4f	v4f_cross(t_vec4f *v1, t_vec4f *v2, t_vec4f *v3)
{
	t_vec4f	v;

	v.x = (v2->z * v3->w - v2->w * v3->z) * v1->y
		+ (v2->w * v3->y - v2->y * v3->w) * v1->z
		+ (v2->y * v3->z - v2->z * v3->y) * v1->w;
	v.y = (v2->z * v3->w - v2->w * v3->z) * v1->x
		+ (v2->w * v3->x - v2->x * v3->w) * v1->z
		+ (v2->x * v3->z - v2->z * v3->x) * v1->w;
	v.z = (v2->y * v3->w - v2->w * v3->y) * v1->x
		+ (v2->w * v3->x - v2->x * v3->w) * v1->y
		+ (v2->x * v3->y - v2->y * v3->x) * v1->w;
	v.w = (v2->y * v3->z - v2->z * v3->y) * v1->x
		+ (v2->z * v3->x - v2->x * v3->z) * v1->y
		+ (v2->x * v3->y - v2->y * v3->x) * v1->z;
	return (v);
}
