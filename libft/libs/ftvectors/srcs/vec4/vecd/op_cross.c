#include "ftvectors/vec4/t_vec4_t.h"

/*
**	E0 = {1, 0, 0, 0}
**	E1 = {0, 1, 0, 0}
**	E2 = {0, 0, 1, 0}
**	E3 = {0, 0, 0, 1}
**
**	V0 = {x0, y0, z0, w0}
**	V1 = {x1, y1, z1, w1}
**	V2 = {x2, y2, z2, w2}
**
**	V3 = (V0 X V1 X V2)
**	   = cross(V0, V1, V2)
**		 | E0 E1 E2 E3 |
**	   = | x0 y0 z0 w0 |
**		 | x1 y1 z1 w1 |
**		 | x2 y2 z2 w2 |
**
**		 | y0 z0 w0 |		 | x0 z0 w0 |
**	   = | y1 z1 w1 | * E0 - | x1 z1 w1 | * E1
**		 | y2 z2 w2 |		 | x2 z2 w2 |
**
**		 | x0 y0 w0 |		 | x0 y0 z0 |
**	   + | x1 y1 w1 | * E2 - | x1 y1 z1 | * E3
**		 | x2 y2 w2 |		 | x2 y2 z2 |
**
**	   = ((z1w2-w1z2)y0-(y1w2-w1y2)z0+(y1z2-z1y2)w0)E0
**		-((z1w2-w1z2)x0-(x1w2-w1x2)z0+(x1z2-z1x2)w0)E1
**		+((y1w2-w1y2)x0-(x1w2-w1x2)y0+(x1y2-y1x2)w0)E2
**		-((y1z2-z1y2)x0-(x1z2-z1x2)y0+(x1y2-y1x2)z0)E3
**
**	   = {
**			(z1w2-w1z2)y0-(y1w2-w1y2)z0+(y1z2-z1y2)w0,
**			-((z1w2-w1z2)x0-(x1w2-w1x2)z0+(x1z2-z1x2)w0),
**			(y1w2-w1y2)x0-(x1w2-w1x2)y0+(x1y2-y1x2)w0,
**			-((y1z2-z1y2)x0-(x1z2-z1x2)y0+(x1y2-y1x2)z0)
**		}
**
**	  (v2->z * v3->w - v2->w * v3->z) * v1->y
**		- (v2->y * v3->w - v2->w * v3->y) * v1->z
**		+ (v2->y * v3->z - v2->z * v3->y) * v1->w
**	-((v2->z * v3->w - v2->w * v3->z) * v1->x
**		- (v2->x * v3->w - v2->w * v3->x) * v1->z
**		+ (v2->x * v3->z - v2->z * v3->x) * v1->w)
**	  (v2->y * v3->w - v2->w * v3->y) * v1->x
**		- (v2->x * v3->w - v2->w * v3->x) * v1->y
**		+ (v2->x * v3->y - v2->y * v3->x) * v1->w
**	-((v2->y * v3->z - v2->z * v3->y) * v1->x
**		- (v2->x * v3->z - v2->z * v3->x) * v1->y
**		+ (v2->x * v3->y - v2->y * v3->x) * v1->z)
**
**	(v2->z * v3->w - v2->w * v3->z) * v1->y
**		+ (v2->w * v3->y - v2->y * v3->w) * v1->z
**		+ (v2->y * v3->z - v2->z * v3->y) * v1->w
**	(v2->z * v3->w - v2->w * v3->z) * v1->x
**		+ (v2->w * v3->x - v2->x * v3->w) * v1->z
**		+ (v2->x * v3->z - v2->z * v3->x) * v1->w
**	(v2->y * v3->w - v2->w * v3->y) * v1->x
**		+ (v2->w * v3->x - v2->x * v3->w) * v1->y
**		+ (v2->x * v3->y - v2->y * v3->x) * v1->w
**	(v2->y * v3->z - v2->z * v3->y) * v1->x
**		+ (v2->z * v3->x - v2->x * v3->z) * v1->y
**		+ (v2->x * v3->y - v2->y * v3->x) * v1->z
*/

t_vec4	v4_cross(t_vec4 *v1, t_vec4 *v2, t_vec4 *v3)
{
	t_vec4	v;

	v.x = (v2->z * v3->w - v2->w * v3->z) * v1->y
		+ (v2->w * v3->y - v2->y * v3->w) * v1->z
		+ (v2->y * v3->z - v2->z * v3->y) * v1->w;
	v.y = (v2->z * v3->w - v2->w * v3->z) * v1->x
		+ (v2->w * v3->x - v2->x * v3->w) * v1->z
		+ (v2->x * v3->z - v2->z * v3->x) * v1->w;
	v.z = (v2->y * v3->w - v2->w * v3->y) * v1->x
		+ (v2->w * v3->x - v2->x * v3->w) * v1->y
		+ (v2->x * v3->y - v2->y * v3->x) * v1->w;
	v.w = (v2->y * v3->z - v2->z * v3->y) * v1->x
		+ (v2->z * v3->x - v2->x * v3->z) * v1->y
		+ (v2->x * v3->y - v2->y * v3->x) * v1->z;
	return (v);
}
