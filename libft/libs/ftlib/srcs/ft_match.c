char	match(const char *s1, const char *s2)
{
	if (!s1 || !s2)
		return (0);
	if (((*s2 == '*' && !s2[1]) || !*s2) && *s1 == '\0')
		return (1);
	if (*s2 == '*' && *s1)
		return (match(s1, s2 + 1) || match(s1 + 1, s2));
	if (*s2 == '*' && !*s1)
		return (match(s1, s2 + 1));
	if (*s2 == *s1)
		return (match(s1 + 1, s2 + 1));
	return (0);
}
